	.global	strcmp
	.type	strcmp,@function
strcmp:
1:
	move.b	[$r10+], $r12
	seq	$r13
	sub.b	[$r11+], $r12
	or.b	$r12, $r13
	beq	1b
	nop

	ret
	movs.b	$r12, $r10
	.size	strcmp, . - strcmp
